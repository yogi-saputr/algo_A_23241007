# perbandingan >, <, <=, >=, ==, !=, is, isnot

x = 3
y = 4

# Lebih besar >
hasil = x > y
print(x, ">", y, "=", hasil)

# Lebih Kecil <
hasil = x < y
print(x, "<", y, "=", hasil)

#Lebih besar >=
hasil = x >= y
print(x, ">=", y, "=", hasil)
print(x, ">=", 3, "=", hasil)

# Lebih kecil <=
hasil = x <= y
print(x, "<=", y, "=", hasil)
print(x, "<=", 3, "=", hasil)

# sama dengan ==
hasil = x == 9
print(x, "==", 9, "=", hasil)

# tidak sama dengan
hasil = x != 9
print(x, "!=", 9, "=", hasil)

# is (adalah)
hasil = x is 9
print(x, "is", 9, "=", hasil)

# isnot 
hasil = x is not 9
print(x, "is not", 9, "=", hasil)